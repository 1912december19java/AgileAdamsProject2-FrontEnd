export class Trainer {
    firstName : string;
    lastName : string;
    location : string;
    curriculum : string;


    constructor(newFirstName : string, newLastName : string, newLocation : string, newCurriculum : string){
        this.firstName = newFirstName;
        this.lastName = newLastName;
        this.location = newLocation;
        this.curriculum = newCurriculum;
    }

}





